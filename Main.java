public class Main {

    public static void main(String[] args) {
        System.out.println("Hello, world!");
    }

    public static print(String s) {
        System.out.println(s);
    }

    public static print(String s, int i) {
        for (int j = 0; j < i; j++) {
            System.out.println(s);
        }
    }
}